constexpr uint8_t charToIndex(char character) {
    // Lowercase to uppercase
    if (character >= 'a' && character <= 'z') { return character - 32 - ' '; }
    if (character >= ' ' && character <= 'Z') { return character - ' '; }
    return 0;
}

